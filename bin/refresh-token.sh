#!/usr/bin/env bash
# Refresh the Coveo search token used by community-mcp.
#
# Default: drives a browser via Playwright. First run is headed (you log in,
# handles MFA naturally); subsequent runs within ~12h are headless and silent.
#
#   bash bin/refresh-token.sh
#
# Manual flow (for users who installed with --no-playwright):
#
#   1. Log in via incognito at https://community.workday.com.
#   2. DevTools -> Network -> reload -> right-click home.html -> Copy -> Copy as cURL (bash).
#   3. pbpaste > .captured-curl.sh
#   4. bash bin/refresh-token.sh --manual
#
# .env, .captured-curl.sh, and .playwright-state.json are all gitignored.

set -euo pipefail

ROOT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")/.." && pwd)"
CURL_FILE="${ROOT_DIR}/.captured-curl.sh"
ENV_FILE="${ROOT_DIR}/.env"
VENV_PYTHON="${ROOT_DIR}/.venv/bin/python"

MODE="auto"
for arg in "$@"; do
    case "$arg" in
        --manual) MODE="manual" ;;
        --auto)   MODE="auto" ;;  # kept for backwards compatibility; same as default
    esac
done

show_expiry() {
    local token="$1"
    "$VENV_PYTHON" - "$token" <<'PYEOF'
import base64, json, datetime, sys
token = sys.argv[1]
payload = token.split('.')[1]
payload += '=' * (-len(payload) % 4)
data = json.loads(base64.urlsafe_b64decode(payload))
exp = datetime.datetime.fromtimestamp(data['exp'])
remaining = exp - datetime.datetime.now()
print(f'  Token expires: {exp.isoformat(timespec="seconds")} (in {int(remaining.total_seconds()/60)} min)')
PYEOF
}

if [[ "$MODE" == "auto" ]]; then
    if [[ ! -x "$VENV_PYTHON" ]]; then
        echo "ERROR: ${VENV_PYTHON} not found. Run bash bin/install.sh first." >&2
        exit 1
    fi
    "$VENV_PYTHON" -m community_mcp.auth
    NEW_TOKEN=$(grep -oE 'COVEO_SEARCH_TOKEN=[^[:space:]]+' "$ENV_FILE" | cut -d= -f2-)
    show_expiry "$NEW_TOKEN"
    exit 0
fi

# Manual flow
if [[ ! -f "$CURL_FILE" ]]; then
    echo "ERROR: ${CURL_FILE} not found." >&2
    echo "  Run: pbpaste > .captured-curl.sh" >&2
    echo "  (after Copy as cURL on the home.html request in DevTools Network)" >&2
    exit 1
fi

SEARCH_TOKEN=$(
    grep -oE 'coveo-info=[^;]+' "$CURL_FILE" \
    | python3 -c '
import sys, urllib.parse, json
raw = sys.stdin.read().strip().split("=", 1)[1]
data = json.loads(urllib.parse.unquote(raw))
print(data["searchToken"])
'
)

if [[ -z "$SEARCH_TOKEN" ]]; then
    echo "ERROR: could not extract searchToken from coveo-info cookie." >&2
    exit 2
fi

umask 077
{
    echo "# Generated by bin/refresh-token.sh --manual — do not commit."
    echo "COVEO_SEARCH_TOKEN=${SEARCH_TOKEN}"
    echo "COVEO_ORG_ID=workdayproductionurv9exm0"
    echo "COVEO_SEARCH_HUB=IPE_AEM"
} > "$ENV_FILE"

echo "Wrote ${ENV_FILE}"
show_expiry "$SEARCH_TOKEN"
